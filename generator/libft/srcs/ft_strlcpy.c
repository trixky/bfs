#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	unsigned int	i;
	unsigned int	a;

	if (*src == '\0')
		return (*dst = '\0');
	a = 0;
	while (src[a])
		a++;
	if (dstsize <= 0)
		return (a);
	i = 0;
	while (src[i] != '\0' && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (a);
}
