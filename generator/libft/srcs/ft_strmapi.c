#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*dest;
	unsigned int	i;

	if (s == NULL || (*f) == NULL)
		return (0);
	i = 0;
	while (s[i])
		i++;
	if (!(dest = malloc(sizeof(*dest) * (i + 1))))
		return (0);
	i = 0;
	while (s[i])
	{
		dest[i] = (*f)(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
